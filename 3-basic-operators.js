/**
 * 1.  Arithmetic operators => used for performing calculations
 * e.g -, + , * % , /
 */

const now = 2022;
const danielAge = 1994;
const nelsonAge = now - 2004;
const currentAge = now - danielAge;
console.log(currentAge);

// END OF MATHEMATICAL OPERANDS

/**
 * 2. Assignment operator
 * e.g = & +=, *=, /=, -=
 */

let initialAmount = 100;
initialAmount += 400;
console.log(initialAmount);

let x = 10;
x *= 0.0012;
console.log(x);
/**
 * Incrementing and decrementing
 * Using ++ and --
 */

let i = 10;
i++;
console.log(i);

/**
 * 3. Comparison operators
 * >, >=, <, <= ==, ===
 * They return boolean result
 */

// NB: typeof is an operator

console.log(nelsonAge > danielAge);
